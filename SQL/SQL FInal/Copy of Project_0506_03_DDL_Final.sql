--Create table statements
-- Create Team table
CREATE TABLE [Game.Team] (
    teamId CHAR(5) NOT NULL,
    teamName VARCHAR(25) NOT NULL,
	CONSTRAINT pk_Team_teamId PRIMARY KEY (teamId)
);

-- Create Opponent table
CREATE TABLE [Game.Opponent] (
    opntId CHAR(5) NOT NULL,
    opntName VARCHAR(25) NOT NULL,
	CONSTRAINT pk_Opponent_opntId PRIMARY KEY (opntId)
);

-- Create Conference table
CREATE TABLE [Game.Conference] (
    confId CHAR(5) NOT NULL,
    confName VARCHAR(30) NOT NULL,
	CONSTRAINT pk_Conference_confId PRIMARY KEY (confId)
);

-- Create Year table
CREATE TABLE [Game.Year] (
    year INT,
	CONSTRAINT pk_Year_year PRIMARY KEY (year)
);

-- Create Location table
CREATE TABLE [Game.Location] (
    locnId CHAR(5) NOT NULL,
    locnName VARCHAR(50),
	CONSTRAINT pk_Location_confId PRIMARY KEY (locnId)
);

-- Create Perform table
CREATE TABLE [Game.Perform](
opntId CHAR(5) NOT NULL,
locnId CHAR(5) NOT NULL,
confId CHAR(5) NOT NULL,
CONSTRAINT pk_Perform_opntId_locnId_confId
PRIMARY KEY (opntId, locnId, confId),
CONSTRAINT fk_Perform_opntId FOREIGN KEY (opntId)
REFERENCES [Game.Opponent] (opntId)
ON DELETE NO ACTION ON UPDATE CASCADE,
CONSTRAINT fk_Perform_locnId FOREIGN KEY (locnId)
REFERENCES [Game.Location] (locnId)
ON DELETE NO ACTION ON UPDATE CASCADE,
CONSTRAINT fk_Perform_confId FOREIGN KEY (confId)
REFERENCES [Game.Conference] (confId)
ON DELETE NO ACTION ON UPDATE CASCADE
);

-- Create Play table
CREATE TABLE [Game.Play](
opntId CHAR(5) NOT NULL,
locnId CHAR(5) NOT NULL,
year INT,
gameDate DATE,
gameTime VARCHAR(25),
gameType VARCHAR (10),
gameTeamScore INT NOT NULL,
gameOpponentScore INT NOT NULL,
CONSTRAINT pk_Play_opntId_locnId_year_gameDate_gameTime
PRIMARY KEY (opntId, locnId, year, gameDate, gameTime),
CONSTRAINT fk_Play_opntId FOREIGN KEY (opntId)
	REFERENCES [Game.Opponent] (opntId)
	ON DELETE NO ACTION ON UPDATE CASCADE,
CONSTRAINT fk_Play_locnId FOREIGN KEY (locnId)
REFERENCES [Game.Location] (locnId)
ON DELETE NO ACTION ON UPDATE CASCADE,
CONSTRAINT fk_Play_year FOREIGN KEY (year)
	REFERENCES [Game.Year] (year)
	ON DELETE NO ACTION ON UPDATE CASCADE
);

